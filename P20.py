import turtle

screen = turtle.Screen()
screen.title("turtle Face")
screen.bgcolor("white")

t = turtle.Turtle()
t.speed(5)
t.width(5)

t.color("lime")
t.penup()
t.goto(0, -150)
t.pendown()
t.circle(150)

t.penup()
t.goto(-60, 40)
t.color("red")
t.begin_fill()
t.circle(30)
t.end_fill()

t.penup()
t.goto(60, 40)
t.color("blue")
t.begin_fill()
t.circle(30)
t.end_fill()

t.penup()
t.goto(-70, -40)
t.color("yellow")
t.begin_fill()
t.forward(140)
t.right(90)
t.forward(20)
t.right(90)
t.forward(140)
t.right(90)
t.forward(20)
t.end_fill()
t.setheading(0)

t.penup()
t.goto(-60, -90)
t.color("black")
t.pendown()
t.begin_fill()
t.goto(0, -160)
t.goto(60, -90)
t.goto(-60, -90)
t.end_fill()

t.hideturtle()
screen.mainloop()
